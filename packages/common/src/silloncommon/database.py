def select_param_user(
    engine: Engine,
    search_key: Optional[List[str]] = None,
    search_id: Optional[List[str]] = None,
) -> List[Union[DbSelectResult, DbSelectResultArtifact]]:
    """Retrieves targeted parameters for the user CLI/Dashboard, parsed into objects.

    Args:
        engine (Engine): The SQLAlchemy engine.
        search_key (Optional[List[str]]): Keys to filter for (e.g., ["learning_rate"]).
        search_id (Optional[List[str]]): Run names to limit the query to.

    Returns:
        List[DbSelectResult]: Clean, parsed dataclasses ready for UI presentation.
    """
    with Session(engine) as session:
        if search_key is not None:
            queries = []
            for key in search_key:
                # Extract ONLY the requested parameter
                query = select(
                    SimulationTable.name,
                    SimulationTable.date,
                    text(f"'{key}'"),
                    SimulationTable.parameters[key],
                )
                query = query.where(
                    SimulationTable.parameters[key] != "null",
                )
                if search_id != []:
                    query = query.where(SimulationTable.name.in_(search_id))
                queries.append(query)
            master_query = union_all(*queries)
        else:
            # Return the full parameters dictionary
            master_query = select(
                SimulationTable.name, SimulationTable.date, SimulationTable.parameters
            )
            master_query = master_query.where(SimulationTable.parameters.is_not({}))
            if search_id != []:
                master_query = master_query.where(SimulationTable.name.in_(search_id))
        out = parse_db_result(session.exec(master_query).all())
        return out


