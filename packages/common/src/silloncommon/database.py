@dataclass
class DbSelectResult:
    """Standardized dataclass for holding queried key-value metrics/parameters.

    Attributes:
        run_name (str): The name of the run.
        date (str): Timestamp of the run.
        key (str): The queried parameter/metric name.
        value (str): The value of the parameter/metric.
    """

    run_name: str
    date: str
    key: str
    value: str


@dataclass
class DbSelectResultArtifact:
    """Standardized dataclass for holding queried artifact pointers.

    Attributes:
        run_name (str): The name of the run.
        date (str): Timestamp of the run.
        value (str): The path or hash pointer of the artifact.
    """

    run_name: str
    date: str
    value: str


def parse_db_result(
    statement_out: List[tuple],
) -> List[Union[DbSelectResult, DbSelectResultArtifact]]:
    """Parses raw SQL tuples from JSON queries into Dataclass objects.

    Args:
        statement_out (List[tuple]): Raw tuple rows returned by a SQLModel execution.

    Returns:
        List[Any]: A list of populated `DbSelectResult` or `DbSelectResultArtifact` objects.
    """
    out = []
    for i in statement_out:
        if len(i) == 3:
            if isinstance(i[2], dict):
                for k, v in i[2].items():
                    out.append(DbSelectResult(i[0], i[1], k, v))
            if isinstance(i[2], str):
                out.append(DbSelectResultArtifact(*i))
        if len(i) == 4:
            out.append(DbSelectResult(*i))
    return out


