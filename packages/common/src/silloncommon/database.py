class SimulationTable(SQLModel, table=True):
    """The core database table representing a single simulation run.

    This table stores all lightweight execution data, including tracking metadata,
    JSON parameters, metrics, and git states. It has a one-to-many relationship
    with `ArtifactTable` for heavy file storage.

    Attributes:
        id (Optional[int]): Primary key generated by the database.
        uuid (str): Unique identifier for the run.
        name (Optional[str]): Human-readable name given to the run.
        date (Optional[str]): Timestamp of when the run occurred.
        parameters (Dict[str, Any]): JSON dictionary of input parameters.
        results (Dict[str, Any]): JSON dictionary of output metrics/values.
        meta_data (Dict[str, Any]): JSON dictionary of environment metadata.
        tag (Optional[List[Any]]): JSON list of user-defined tags.
        note (Optional[List[Any]]): JSON list of user notes.
        executiontime (Optional[str]): The duration of the run.
        status (Optional[str]): Run status (e.g., "SUCCESS", "FAILURE", "KILLED").
        platform (Optional[str]): Environment type (e.g., "python", "cpp").
        hostname (Optional[str]): Network name of the machine executing the run.
        isdirty (Optional[bool]): True if uncommitted changes existed during the run.
        organisation (Optional[str]): Organization associated with the run.
        author (Optional[str]): Author/user who triggered the run.
        project (Optional[str]): Project name of the `.sillon` repository.
        hashes (Optional[str]): Dictionarry of all the hashes of the simulation
        sillonversion (Optional[str]): Version of Simtrack used to record the run.
        artifacts (List[ArtifactTable]): Linked artifacts generated by this run.
    """

    id: Optional[int] = Field(default=None, primary_key=True)
    uuid: str
    name: str
    date: Optional[str]

    # JSON Data Columns
    parameters: Dict[str, Any] = Field(default_factory=dict, sa_column=Column(JSON))
    results: Dict[str, Any] = Field(default_factory=dict, sa_column=Column(JSON))
    meta_data: Dict[str, Any] = Field(default_factory=dict, sa_column=Column(JSON))
    tag: Optional[List[Any]] = Field(default_factory=list, sa_column=Column(JSON))
    note: Optional[List[Any]] = Field(default_factory=list, sa_column=Column(JSON))

    # Execution & Environment Metadata
    runtime: Optional[str]
    status: Optional[str]
    platform: str
    hostname: str
    # isdirty: Optional[bool]
    organisation: Optional[str]
    author: Optional[str]
    project: Optional[str]
    hashes: Dict[str, Any] = Field(default_factory=dict, sa_column=Column(JSON))
    sillonversion: Optional[str]

    # Relationships
    artifacts: List["ArtifactTable"] = Relationship(back_populates="simulation")
    figures: List["FigureTable"] = Relationship(back_populates="simulation")
    analyses: List["AnalysisTable"] = Relationship(back_populates="simulation")


class ArtifactTable(SQLModel, table=True):
    """Represents a physical file (blob) generated by a simulation run.

    Attributes:
        id (Optional[int]): Primary key.
        name (Optional[str]): Human-readable filename.
        hash (str): The SHA hash used for content-addressable storage deduplication.
        path (str): The physical path where the blob is stored.
        type (Optional[str]): Categorization tag (e.g., "plot", "mesh", "log").
        size (Optional[str]): File size representation.
        run_id (Optional[int]): Foreign key linking to the parent SimulationTable.
        simulation (Optional[SimulationTable]): Relationship to the parent run.
    """

    id: Optional[int] = Field(default=None, primary_key=True)
    name: Optional[str]
    hsh: str = Field(index=True)
    path: str
    # type: Optional[str]
    # size: Optional[str]

    # Relationships
    run_id: Optional[int] = Field(default=None, foreign_key="simulationtable.id")
    simulation: Optional[SimulationTable] = Relationship(back_populates="artifacts")


class FigureTable(SQLModel, table=True):
    """Represents a figure produced and tracked during a simulation run.

    A figure is stored on disk like an artifact (content-addressable copy)
    but carries its own provenance metadata: which parameters and results
    were used to draw it, a caption, the file format... This is what lets a
    user answer "what data was used for this figure?" months later.

    Attributes:
        id (Optional[int]): Primary key.
        name (Optional[str]): Human-readable figure name.
        hsh (str): The SHA hash of the figure file for integrity tracking.
        path (str): The storage pointer of the figure file.
        meta (Dict[str, Any]): Provenance metadata. Reserved keys: `used`
            (list of parameter/result names the figure was built from),
            `caption`, `format`.
        date (Optional[str]): Timestamp of when the figure was logged.
        run_id (Optional[int]): Foreign key linking to the parent run.
        simulation (Optional[SimulationTable]): Relationship to the parent run.
    """

    id: Optional[int] = Field(default=None, primary_key=True)
    name: Optional[str]
    hsh: str = Field(index=True)
    path: str
    meta: Dict[str, Any] = Field(default_factory=dict, sa_column=Column(JSON))
    date: Optional[str]

    # Relationships
    run_id: Optional[int] = Field(default=None, foreign_key="simulationtable.id")
    simulation: Optional[SimulationTable] = Relationship(back_populates="figures")


class AnalysisTable(SQLModel, table=True):
    """Represents post-processed data attached to a run after the fact.

    Unlike results, analyses are not produced by the simulation itself: they
    are computed later (e.g., evaluating a fitted function on a new grid) and
    linked back to the run for reuse. The heavy data lives in the run's HDF5
    glob under the `analysis` group; this row holds the pointer and context.

    Attributes:
        id (Optional[int]): Primary key.
        name (Optional[str]): The analysis name (also the glob dataset name).
        hsh (str): The SHA hash of the analysis data.
        pointer (str): The dataset pointer inside the run's glob.
        meta (Dict[str, Any]): Free-form context (inputs used, comment...).
        date (Optional[str]): Timestamp of when the analysis was attached.
        run_id (Optional[int]): Foreign key linking to the parent run.
        simulation (Optional[SimulationTable]): Relationship to the parent run.
    """

    id: Optional[int] = Field(default=None, primary_key=True)
    name: Optional[str]
    hsh: str = Field(index=True)
    pointer: str
    meta: Dict[str, Any] = Field(default_factory=dict, sa_column=Column(JSON))
    date: Optional[str]

    # Relationships
    run_id: Optional[int] = Field(default=None, foreign_key="simulationtable.id")
    simulation: Optional[SimulationTable] = Relationship(back_populates="analyses")


