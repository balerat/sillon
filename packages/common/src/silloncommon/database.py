def check_hashes_exists(input_hash: str, engine: Engine) -> Optional[Tuple[str, str]]:
    """Checks the database to see if a specific file blob hash already exists.

    Crucial for content-addressable storage to prevent duplicating massive files.

    Args:
        input_hash (str): The SHA hash of the file.
        engine (Engine): The SQLAlchemy engine.

    Returns:
        Optional[tuple]: The path and hash if it exists, otherwise None.
    """
    with Session(engine) as session:
        statement = select(ArtifactTable).where(ArtifactTable.hsh == input_hash)
        results = session.exec(statement).first()
        if results is None:
            return results
        else:
            return results.path, results.hsh


