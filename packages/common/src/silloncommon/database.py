def select_all(engine: Engine, run_id: Optional[str] = None):
    with Session(engine) as session:
        # Start with the base query
        query = select(SimulationTable)

        # Only add the filter if a specific run_id was provided
        if run_id is not None:
            query = query.where(SimulationTable.uuid == run_id)

        return session.exec(query).all()


def select_param(engine, search_key=None):
    with Session(engine) as session:
        if search_key is not None:
            queries = []
            for key in search_key:
                # Extract ONLY the requested parameter
                query = select(
                    SimulationTable.uuid,
                    text(f"'{key}'"),
                    SimulationTable.parameters[key],
                )
                query = query.where(
                    SimulationTable.parameters[key] != "null",
                )
                queries.append(query)
            master_query = union_all(*queries)
        else:
            # Return the full parameters dictionary
            master_query = select(SimulationTable.uuid, SimulationTable.parameters)
            master_query = master_query.where(SimulationTable.parameters.is_not({}))

        return session.exec(master_query).all()


