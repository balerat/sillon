def db_append_note_tag(
    engine, run_names: List[str], notes: List[str] = None, tags: List[str] = None
) -> List[str]:
    """
    Strictly handles the SQLite updating logic.
    Returns a list of the run names that were successfully updated.
    """
    updated_runs = []
    with Session(engine) as session:
        query = select(SimulationTable).where(SimulationTable.name.in_(run_names))
        runs = session.exec(query).all()

        for run in runs:
            if notes:
                run.note.extend(notes)
                flag_modified(run, "note")  # SQL-specific hack stays hidden here!

            if tags:
                run.tag.extend(tags)
                flag_modified(run, "tag")

            session.add(run)
            updated_runs.append(run.name)

        session.commit()

    return updated_runs


def db_append_metadata(
    engine, run_names: List[str], metadata: Dict[str, Any]
) -> List[str]:
    """Merges new metadata keys into the meta_data column of existing runs.

    Args:
        engine (Engine): The SQLAlchemy engine.
        run_names (List[str]): The run names or uuids to update.
        metadata (dict): The metadata key/value pairs to merge in.

    Returns:
        List[str]: The names of the runs that were successfully updated.
    """
    updated_runs = []
    with Session(engine) as session:
        query = select(SimulationTable).where(
            (SimulationTable.name.in_(run_names))
            | (SimulationTable.uuid.in_(run_names))
        )
        runs = session.exec(query).all()

        for run in runs:
            run.meta_data.update(metadata)
            flag_modified(run, "meta_data")
            session.add(run)
            updated_runs.append(run.name)

        session.commit()

    return updated_runs
