def db_delete_runs(engine: Engine, identifiers: List[str]) -> List[str]:
    """Deletes runs and their linked rows from the database.

    Removes the matching `SimulationTable` rows along with their artifacts,
    figures, and analyses. Matches on run name or uuid.

    Args:
        engine (Engine): The SQLAlchemy engine.
        identifiers (List[str]): Run names or uuids to delete.

    Returns:
        List[str]: The names of the runs that were deleted.
    """
    SQLModel.metadata.create_all(engine)  # Ensure linked tables exist (older DBs)
    deleted = []
    with Session(engine) as session:
        runs = session.exec(
            select(SimulationTable).where(
                (SimulationTable.name.in_(identifiers))
                | (SimulationTable.uuid.in_(identifiers))
            )
        ).all()

        for run in runs:
            for table in (ArtifactTable, FigureTable, AnalysisTable):
                linked = session.exec(
                    select(table).where(table.run_id == run.id)
                ).all()
                for row in linked:
                    session.delete(row)
            session.delete(run)
            deleted.append(run.name)

        session.commit()

    return deleted


def next_available_name(engine: Engine, name: str, taken: Sequence[str] = ()) -> str:
    """Returns a unique run name, appending an increment on collision.

    A name is considered taken if it already exists in the database or appears
    in `taken` (in-flight runs not yet committed). The first free name in the
    series `name`, `name_2`, `name_3`, ... is returned.

    Args:
        engine (Engine): The SQLAlchemy engine.
        name (str): The desired base run name.
        taken (Sequence[str]): Extra names to treat as already used (e.g. runs
            currently registered on the server but not yet dumped).

    Returns:
        str: A run name guaranteed not to collide with existing or in-flight runs.
    """
    with Session(engine) as session:
        existing = set(session.exec(select(SimulationTable.name)).all())
    existing.update(taken)

    if name not in existing:
        return name

    index = 2
    while f"{name}_{index}" in existing:
        index += 1
    return f"{name}_{index}"


