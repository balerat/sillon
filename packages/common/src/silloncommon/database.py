def insert_multiple_simulations(
    simulations: Dict[str, "Simulation"], session: Session
) -> List[SimulationTable]:
    """Saves a dictionary of multiple simulation objects to the database.

    Iterates over a collection of runs, splits their standard metrics from
    heavy artifacts, and constructs the relational rows before bulk-committing
    them to SQLite.

    Args:
        simulations (dict): A dictionary mapping run IDs to run objects.
        session (Session): The active SQLModel database session.

    Returns:
        List[SimulationTable]: A list of the successfully committed database rows.
    """
    saved_tables = []

    for run_id, run in simulations.items():
        clean_results = {}
        # We create a list to hold all the artifacts for this specific run
        run_artifacts: List[ArtifactTable] = []

        for res_name, res_item in run.results.items():
            if not res_item.is_artifact:
                # CASE 1: It's just a value or a pointer (not a file)
                clean_results[res_name] = res_item.value
            else:
                # CASE 2: IT IS AN ARTIFACT!
                # We create the ArtifactTable row object
                new_artifact = ArtifactTable(
                    name=res_name,
                    hsh=res_item.hsh,
                    path=res_item.value,
                )
                run_artifacts.append(new_artifact)

        # Create the Simulation Database Row
        table = SimulationTable(
            uuid=run_id,
            name=run.run_name,
            date=run.date,
            parameters=run.parameters,
            results=clean_results,
            meta_data=run.metadata,
            tag=run.tags,
            note=run.notes,
            artifacts=run_artifacts,
            platform=run.platform,
            hostname=run.hostname,
            author=run.author,
            hashes=asdict(run.hashes),
            organisation=run.organisation,
            project=run.project_name,
            sillonversion=sillon_VERSION,
            status=run.status,
            runtime=run.runtime,
        )

        session.add(table)
        saved_tables.append(table)

    # When we hit commit, SQLModel will:
    # 1. Save the SimulationTable and get its new 'id'
    # 2. Automatically assign that 'id' to the 'run_id' of every artifact in the list
    # 3. Save all the ArtifactTables to the database.
    session.commit()

    return saved_tables


def insert_simulation(run: "Simulation", session: Session) -> SimulationTable:
    """Saves a single simulation object to the database.

    Args:
        run (Any): The simulation data object.
        session (Session): The active SQLModel database session.

    Returns:
        SimulationTable: The successfully committed database row for the run.
    """
    clean_results = {}
    # We create a list to hold all the artifacts for this specific run
    run_artifacts: List[ArtifactTable] = []

    for res_name, res_item in run.results.items():
        if not res_item.is_artifact:
            # CASE 1: It's just a value or a pointer (not a file)
            clean_results[res_name] = res_item.value
        else:
            # CASE 2: IT IS AN ARTIFACT!
            # We create the ArtifactTable row object
            new_artifact = ArtifactTable(
                name=res_name,
                hsh=res_item.hsh,
                path=res_item.value,
            )
            run_artifacts.append(new_artifact)

    # Figures logged during the run (each carries its provenance metadata)
    run_figures: List[FigureTable] = [
        FigureTable(
            name=fig_name,
            hsh=fig_item.hsh,
            path=fig_item.value,
            meta=fig_item.meta,
            date=run.date,
        )
        for fig_name, fig_item in getattr(run, "figures", {}).items()
    ]

    # Create the Simulation Database Row
    table = SimulationTable(
        uuid=run.run_id,
        name=run.run_name,
        date=run.date,
        parameters=run.parameters,
        results=clean_results,
        meta_data=run.metadata,
        tag=run.tags,
        note=run.notes,
        artifacts=run_artifacts,
        figures=run_figures,
        platform=run.platform,
        hostname=run.hostname,
        author=run.author,
        hashes=asdict(run.hashes),
        organisation=run.organisation,
        project=run.project_name,
        sillonversion=sillon_VERSION,
        status=run.status,
        runtime=run.runtime,
    )

    session.add(table)

    # When we hit commit, SQLModel will:
    # 1. Save the SimulationTable and get its new 'id'
    # 2. Automatically assign that 'id' to the 'run_id' of every artifact in the list
    # 3. Save all the ArtifactTables to the database.
    session.commit()

    return table


